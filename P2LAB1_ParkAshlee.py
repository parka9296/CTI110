# Program to calculate measurements of a circle


import math 
r=8.75
p=math.pi
d=2*r
c=2*p*r
a=p*r**2


print("What is the radius of the circle?", 8.75, "\n")

print(f"The diameter of the circle is {d:.1f}", "\n")

print(f"The circumference of the circle is {c:.2f}", "\n")

print(f"The area of the circle is {a:.3f}")
